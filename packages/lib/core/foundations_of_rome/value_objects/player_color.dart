// Colores ANSI usados por la version de consola para distinguir jugadores.

/// Constantes de color para imprimir texto coloreado en terminal.
class ColorJugador {
  static const String reset = '\x1B[0m';
  static const String rojo = '\x1B[31m'; // Jugador 1
  static const String verde = '\x1B[32m'; // Jugador 2
  static const String amarillo = '\x1B[33m'; // Jugador 3
  static const String azul = '\x1B[34m'; // Jugador 4
  static const String magenta = '\x1B[35m'; // Jugador 5
  static const String cian = '\x1B[36m'; // Info sistema
}
